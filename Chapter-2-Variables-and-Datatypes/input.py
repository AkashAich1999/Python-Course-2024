# Concatenation using input() Function
a = input("Enter Number 1:")
b = input("Enter Number 2:")

print("Number 1:", a)
print("Number 2:", b)
print("Sum =", a + b)

# Sum using input() Function
a = int(input("Enter Number 1:"))
b = int(input("Enter Number 2:"))

print("Number 1:", a)
print("Number 2:", b)
print("Sum =", a + b)